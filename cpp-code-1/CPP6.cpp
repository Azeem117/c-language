//increment and decrement
#include <iostream>
using namespace std;

int main() {
  int x = 4;cout<<x<<endl;
  x++;cout<<x<<endl;
  //if we have to increase value by one we use x++;
  //if we have to decrease value by one we use x--;
  x--;cout<<x<<endl;
  int x = 4;cout<<x<<endl;cout<<x++<<endl;cout<<x<<endl;
  
}
